DECLARE

CFG_LISTA_ID RENIEC.CFG_LISTA.ID%TYPE := 0;
CFG_VALOR_ID RENIEC.CFG_VALOR.ID%TYPE := 0;
CTL_CONSULTANTE_ID RENIEC.CTL_CONSULTANTE.ID%TYPE := 0;
SEG_MEMBRESIA_ID RENIEC.SEG_MEMBRESIA.ID%TYPE := 0;
SEG_OPCION_ID RENIEC.SEG_OPCION.ID%TYPE := 0;
SEG_PERMISO_ID RENIEC.SEG_PERMISO.ID%TYPE := 0;
SEG_ROL_ID RENIEC.SEG_ROL.ID%TYPE := 0;
SEG_USUARIO_ID RENIEC.SEG_USUARIO.ID%TYPE := 0;

BEGIN

	DELETE FROM RENIEC.SEQ_ENTIDAD;
	SELECT NVL(MAX(ID),0) into CFG_LISTA_ID FROM RENIEC.CFG_LISTA;
	SELECT NVL(MAX(ID),0) into CFG_VALOR_ID FROM RENIEC.CFG_VALOR;
	SELECT NVL(MAX(ID),0) into CTL_CONSULTANTE_ID FROM RENIEC.CTL_CONSULTANTE;
	SELECT NVL(MAX(ID),0) into SEG_MEMBRESIA_ID FROM RENIEC.SEG_MEMBRESIA;
	SELECT NVL(MAX(ID),0) into SEG_OPCION_ID FROM RENIEC.SEG_OPCION;
	SELECT NVL(MAX(ID),0) into SEG_PERMISO_ID FROM RENIEC.SEG_PERMISO;
	SELECT NVL(MAX(ID),0) into SEG_ROL_ID FROM RENIEC.SEG_ROL;
	SELECT NVL(MAX(ID),0) into SEG_USUARIO_ID FROM RENIEC.SEG_USUARIO;

	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Lista', CFG_LISTA_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Valor', CFG_VALOR_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Consultante', CTL_CONSULTANTE_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Membresia', SEG_MEMBRESIA_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Opcion', SEG_OPCION_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Permiso', SEG_PERMISO_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Rol', SEG_ROL_ID);
	INSERT into RENIEC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.RENIEC.dominio.Usuario', SEG_USUARIO_ID);

	COMMIT;

END;
