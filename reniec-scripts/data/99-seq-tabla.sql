DECLARE

CFG_LISTA_ID CONREC.CFG_LISTA.ID%TYPE := 0;
CFG_VALOR_ID CONREC.CFG_VALOR.ID%TYPE := 0;
CTL_CONSULTANTE_ID CONREC.CTL_CONSULTANTE.ID%TYPE := 0;
SEG_MEMBRESIA_ID CONREC.SEG_MEMBRESIA.ID%TYPE := 0;
SEG_OPCION_ID CONREC.SEG_OPCION.ID%TYPE := 0;
SEG_PERMISO_ID CONREC.SEG_PERMISO.ID%TYPE := 0;
SEG_ROL_ID CONREC.SEG_ROL.ID%TYPE := 0;
SEG_USUARIO_ID CONREC.SEG_USUARIO.ID%TYPE := 0;

BEGIN

	DELETE FROM CONREC.SEQ_ENTIDAD;
	SELECT NVL(MAX(ID),0) into CFG_LISTA_ID FROM CONREC.CFG_LISTA;
	SELECT NVL(MAX(ID),0) into CFG_VALOR_ID FROM CONREC.CFG_VALOR;
	SELECT NVL(MAX(ID),0) into CTL_CONSULTANTE_ID FROM CONREC.CTL_CONSULTANTE;
	SELECT NVL(MAX(ID),0) into SEG_MEMBRESIA_ID FROM CONREC.SEG_MEMBRESIA;
	SELECT NVL(MAX(ID),0) into SEG_OPCION_ID FROM CONREC.SEG_OPCION;
	SELECT NVL(MAX(ID),0) into SEG_PERMISO_ID FROM CONREC.SEG_PERMISO;
	SELECT NVL(MAX(ID),0) into SEG_ROL_ID FROM CONREC.SEG_ROL;
	SELECT NVL(MAX(ID),0) into SEG_USUARIO_ID FROM CONREC.SEG_USUARIO;

	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Lista', CFG_LISTA_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Valor', CFG_VALOR_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Consultante', CTL_CONSULTANTE_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Membresia', SEG_MEMBRESIA_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Opcion', SEG_OPCION_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Permiso', SEG_PERMISO_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Rol', SEG_ROL_ID);
	INSERT into CONREC.SEQ_ENTIDAD (ENTIDAD, ULTIMO_ID) VALUES ('pe.com.bbva.reniec.dominio.Usuario', SEG_USUARIO_ID);

	COMMIT;

END;
